function RootShell({ children }: { children: React.ReactNode }) {
  return (
    <html lang="en">
      <head>
        <HeadContent />
      </head>
      <body>
        {children}
        <Scripts />
      </body>
    </html>
  );
}

function AuthSync() {
  const queryClient = useQueryClient();
  const router = useRouter();
  useEffect(() => {
    const onAuth = () => {
      queryClient.invalidateQueries();
      router.invalidate();
    };
    window.addEventListener("gk-auth-change", onAuth);
    return () => window.removeEventListener("gk-auth-change", onAuth);
  }, [queryClient, router]);
  return null;
}

function RootComponent() {
  const { queryClient } = Route.useRouteContext();
  return (
    <QueryClientProvider client={queryClient}>
      <LanguageProvider>
        <AuthSync />
        <Outlet />
        <Toaster richColors position="top-right" />
        <Analytics />
      </LanguageProvider>
    </QueryClientProvider>
  );
}
